public class MultiplicationTable {

    public static void main(String[] args) { 

        for(int i = 1; i<=10; i++){ 
            for(int j = 1; j<=10; j++){ 
               // System.out.println(i + " * "+ j + " = " + j*i);
               System.out.print(" " + j*i + " ");
            }
            System.out.println();
        }
    }
    
}
